import math
res=math.sqrt(81)
print(f"square root of 81 is: {res}")

res1=math.pow(9,3)
print(f"9 raised to the power of 3 is: {res1}")

angle=math.pi/4  #45deg in radius

print(math.sin(angle))
print(math.cos(angle))

res2=math.factorial(6)
print(f"factorial of 6 is : {res2}")

num=4.7
num1=3.3
print(math.ceil(num))
print(math.ceil(num1))
print(math.floor(num))
print(math.floor(num1))

res3=math.gcd(81,27)
print(f"gcd of 81 and 27 is : {res3}")